        call getarg(1,lsffile)
        call getarg(2,obsarg)
        call getarg(3,marg)
        call getarg(4,karg)
        read(obsarg,'(i10)') obsin
        read(marg,'(i10)') min
        read(karg,'(i10)') kin

        open(10,file=lsffile,status='old')
        do
          read(10,*,iostat=stat) obs,m,k,(lsfq(q),q=1,_QDIM_)
