      subroutine lsfq2lsfj(lsfq,lsfj)
      implicit none
 
      real  lsfq(013)
      real  lsfj(201)
      real*8 summ, sumj
 
      integer j, q, k
 
      integer*2  le_jq_x10000(13,201)
      common /le_jq_x10000_/le_jq_x10000
 
      summ = 0.
      do j = 001, 201
         sumj = 0.
         do q = 01, 13
            sumj = sumj + lsfq(q)*le_jq_x10000(q,j)/1.0e5
            enddo
         lsfj(j) = sumj
         summ = summ + sumj
         enddo
      do j = 1, 201
         lsfj(j) = lsfj(j)/summ
         enddo
      do q = 1, 13
         lsfq(q) = lsfq(q)/summ
         enddo
 
      return
      end

 
